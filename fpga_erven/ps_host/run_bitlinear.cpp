//
// Usage
// -----
//   g++ -O2 -o run_bitlinear run_bitlinear.cpp
//   sudo ./run_bitlinear [path/to/reference/test_vectors]
//
// AXI address map — UPDATE AFTER VIVADO ADDRESS EDITOR
// -----------------------------------------------------
//   Open Vivado → Block Design → Address Editor after block design is complete.
//   Replace the two placeholder values below with the actual assigned offsets.
//
//   CTRL_PHYS_BASE    → s_axi_CTRL    (ap_start/done, M, K)
//   CONTROL_PHYS_BASE → s_axi_control (x, W_packed, y base addresses)
//
// Register map (from xbitlinear_hls_hw.h, do not edit)
// -----------------------------------------------------
//   s_axi_CTRL:
//     0x00  ap_ctrl   bit0=ap_start, bit1=ap_done, bit2=ap_idle
//     0x10  M         (scalar, 32-bit)
//     0x18  K         (scalar, 32-bit)
//   s_axi_control:
//     0x10  x_lo      x[] physical address [31:0]
//     0x14  x_hi      x[] physical address [63:32]
//     0x1C  W_lo      W_packed[] physical address [31:0]
//     0x20  W_hi      W_packed[] physical address [63:32]
//     0x28  y_lo      y[] physical address [31:0]
//     0x2C  y_hi      y[] physical address [63:32]
// =============================================================================

