// =============================================================================
// AXI base addresses — FILL FROM VIVADO ADDRESS EDITOR BEFORE RUNNING
// =============================================================================
// These are the physical addresses of the two AXI-Lite slave interfaces
// as assigned by Vivado. Default range is 0xA0000000–0xAFFFFFFF for ZCU106 PL.

#define CTRL_PHYS_BASE     0xA0000000ULL   // s_axi_CTRL  — TBD: check Vivado addr editor
#define CONTROL_PHYS_BASE  0xA0010000ULL   // s_axi_control — TBD: check Vivado addr editor
#define AXILITE_MAP_SIZE   0x10000UL       // 64 KB per interface

// =============================================================================
// Register offsets (from xbitlinear_hls_hw.h — generated by Vitis HLS)
// =============================================================================

// s_axi_CTRL register offsets
#define CTRL_AP_CTRL   0x00
#define CTRL_M_DATA    0x10
#define CTRL_K_DATA    0x18

// ap_ctrl bit positions
#define AP_START_BIT   (1U << 0)
#define AP_DONE_BIT    (1U << 1)
#define AP_IDLE_BIT    (1U << 2)

// s_axi_control register offsets (pointer arguments)
#define CTL_X_LO       0x10
#define CTL_X_HI       0x14
#define CTL_W_LO       0x1C
#define CTL_W_HI       0x20
#define CTL_Y_LO       0x28
#define CTL_Y_HI       0x2C

// =============================================================================
// DMA buffer layout
// =============================================================================
// All three buffers are allocated as a single contiguous page-aligned block
// to guarantee they land in physically contiguous pages with a single mlock.
// Sizes are set to the HLS depth pragmas (worst-case: 512-element x, 256K-byte W).

#define MAX_M           512
#define MAX_K           1024
#define MAX_PACKED_SZ   (MAX_M * MAX_K / 4)  // = 131 072 bytes

// Layout within the single DMA allocation:
//   [0 .. MAX_K-1]                     : x_buf   (int8_t, MAX_K bytes)
//   [MAX_K .. MAX_K + MAX_PACKED_SZ-1] : W_buf   (uint8_t, MAX_PACKED_SZ bytes)
//   aligned to next page               : y_buf   (int32_t, MAX_M * 4 bytes)

#define X_OFFSET        0
#define W_OFFSET        (MAX_K)
#define Y_OFFSET        ((MAX_K + MAX_PACKED_SZ + 4095) & ~4095UL)
#define DMA_TOTAL_SIZE  (Y_OFFSET + MAX_M * sizeof(int32_t))

// =============================================================================
// Poll timeout
// =============================================================================
#define POLL_TIMEOUT_MS   5000   // 5 seconds

// =============================================================================
// Test vector directory (overridable via argv[1])
// =============================================================================
